public class aktivitas {

	public static void main(String[] args) {

		double panjang = 25;
		double lebar = 10;
		double jumlah, kurang, kali, bagi, modulus;

		jumlah = panjang + lebar;
		kurang = panjang - lebar;
		kali = panjang * lebar;
		bagi = panjang / lebar;
		modulus = panjang % lebar;

		System.out.println("Hasil penjumlahan\t:"+jumlah);
		System.out.println("Hasil pengurangan\t:"+kurang);
		System.out.println("Hasil perkalian\t\t:"+kali);
		System.out.println("Hasil pembagi\t\t:"+bagi);
		System.out.println("Hasil modulus\t\t:"+modulus);

	}

}
