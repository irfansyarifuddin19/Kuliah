public class contoh_for {

	public static void main(String[] args) {
		
		System.out.println("Perulangan for naik");
		for (int n = 1; n <= 5; ++n ) {
			
			System.out.println("naik: " + n);

		}
		System.out.println("Perulangan for turun");
		for (int i = 101; i <= 909; i += 101 ) {
			
			System.out.print(i + " ");

		}
		System.out.println();

	}
	
}